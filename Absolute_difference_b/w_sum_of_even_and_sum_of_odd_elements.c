#include<stdio.h>
int main()
{
    int n,a[100],i,sum1=0,sum2=0,p;
    scanf("%d",&n);
    for(i=0;i<n;i++)
    {
        scanf("%d",&a[i]);
    }
    for(i=0;i<n;i++)
    {
        if(a[i]%2==0)
        {
            sum1+=a[i];
        }
    }
    for(i=0;i<n;i++)
    {
        if(a[i]%2!=0)
        {
            sum2+=a[i];
        }
    }
    p=sum2-sum1;
    if(p>0)
    {
        printf("%d",p);
    }
    else
    {
        printf("%d",-p);
    }
}